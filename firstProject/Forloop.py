# The Forloop runs a code in a specified number of times.
# Forloop basically have two components: the first component is the number of times a code is executed.
# The second component is the block of code.

# Example.
# for range(-, -, -): first component. range has three components.
# block of code. second component

# The forloop range is made up of three components:
# 1. starting point.
# 2. stoping point.
# 3. increment or step.
# Example.
# for i in range(1,8,2):
#   block of code.

for i in range(1, 8, 2):
    print("still looping")
    print(i)
print("The loop ends here")
