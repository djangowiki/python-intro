# Lists.
# A python list has the following 4 main attributes:
# 1. A list is ordered. that is it starts from most important to least important.
# 2. A list is indexed. that is to say, given a number, you can tell where the list item is.
# 3. A list allows for duplicates. that is to say that items in the list can occur twice or more.
# 4. A list is changeable. that is to say that a list item can be changed or it is mutable.

